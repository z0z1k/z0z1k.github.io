
					function gameStart1() {
						var apiname = $("#gameKit1")["val"]();
						var gameUit = $("#gameUid")["val"]();
						$.ajax({
							type: "POST",
							url: "/system/function.php",
							data: {
								apiname: apiname,
								gameUit: gameUit
							},
							success: function(html) {
								$("#result").empty();
								$("#result").append(html);
							}
						})
					}
